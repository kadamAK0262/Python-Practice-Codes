# Using the reverse() method:

my_list = [1, 2, 3, 4, 5]
my_list.reverse()
print("Reversed list:", my_list)


# Using the slicing :

my_list = [1, 2, 3, 4, 5]
reversed_list = my_list[::-1]
print("Reversed list:", reversed_list)

# Using the reversed() function with list():

my_list = [1, 2, 3, 4, 5]
reversed_list = list(reversed(my_list))
print("Reversed list:", reversed_list)