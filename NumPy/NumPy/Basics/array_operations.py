import numpy as np

arr1 = np.array([1, 2, 3])
arr2 = np.array([4, 5, 6])

# addition
result_add = arr1 + arr2

# subtraction
result_sub = arr1 - arr2

#  multiplication
result_mul = arr1 * arr2

# division
result_div = arr1 / arr2

print("Addition:")
print(result_add)
print("Subtraction:")
print(result_sub)
print("Multiplication:")
print(result_mul)
print("Division:")
print(result_div)